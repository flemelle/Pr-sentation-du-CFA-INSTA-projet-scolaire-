        <h3> INSTA, UN TREMPLIN POUR LA RÉUSSITE PROFESSIONNELLE</h3> 
        <p>Créé en 2009 par Ryad Lebib et Hubert Trapet, le CFA INSTA accueille et forme près de 600 étudiant(e)s par an pour leur assurer un avenir professionnel. Notre réactivité et notre écoute du marché s’est traduite par la mise en place de formations adaptées et diplômantes reconnues par l’État (titres RNCP et diplômes d’État), en informatique (développement, logiciel et réseaux), en ressources humaines et en comptabilité. Depuis près de 10 ans, le CFA INSTA propose plusieurs formations en alternance (contrat d’apprentissage ou de professionnalisation) en comptabilité et RH et en initiale et en alternance en informatique.</p>
        <p>Nos formations vont de bac+ 2 à bac+5. La force du CFA INSTA, est de s’être spécialisé depuis sa création, dans l’accompagnement des étudiant(e)s et la prise en compte de chaque situation et motivation pour leur offrir un vrai tremplin pour leur projet d’avenir professionnel. En effet, notre but est de permettre aux étudiant(e)s d’acquérir à la fois un diplôme reconnu sur le marché de l’emploi, mais également une expérience professionnelle en adéquation avec ce diplôme grâce à l’alternance et au stage pour le BTS SIO en initiale.</p>
        <p>Nous avons également comme valeur d’aider les personnes en rupture ou en échec scolaire, de se réinserer dans le milieu du travail à travers nos titres professionnels et les passerelles universitaires. Nous proposons également des stages d’initiations aux futur(e)s étudiant(e)s en BTS SIO et en BTS CG. Notre équipe compte plus de 100 intervenants et experts d’origine universitaire et professionnelle. Nos intervenants se sont distingués sur le plan international à travers leurs interventions professionnelles auprès des entreprises et leurs différentes publications sur différents thèmes (technique, management, RH, etc.).</p>
        <p>Enfin, nous plaçons au cœur de notre pédagogie la notion de projet, chaque séquence de formation étant couronnée par une soutenance à laquelle sont associés les tuteurs et maîtres d’apprentissage pour les étudiant(e)s en alternance, ce qui permet de renforcer les liens avec l’environnement professionnel de nos stagiaires et de mesurer constamment la pertinence de nos formations afin qu’elles restent adaptées au marché.</p>
